        <div class="faq-question">
            <div class="faq-question-content">
                <div class="question-text">Apa yang harus pihak panti sosial lakukan setelah mendapatkan notifikasi pendaftaran donatur atau relawan pada salah satu kegiatan yang telah dibuat?</div>
                <svg class="arrow-icon" viewBox="0 0 24 24">
                    <path d="M7 10l5 5 5-5z"></path>
                </svg>
            </div>
            <div class="answer">
                Panti sosial akan mendapatkan notifikasi pendaftaran donatur atau relawan melalui email lengkap
                dengan detail profil donatur atau relawan. Langkah selanjutnya yang harus pihak panti sosial
                lakukan berbeda tergantung pada jenis pendaftar, yaitu donatur atau relawan. Berikut detail
                 hal yang harus dilakukan oleh panti sosial.
                 <br>
                 <br>

             <span style="font-weight: bold;">KEGIATAN DONASI:</span> <br>
             Apabila notifikasi berasal dari pendaftaran donatur, maka langkah selanjutnya yang pihak panti sosial harus lakukan adalah menghubungi secara langsung melalui nomor WhatsApp yang telah dicantumkan. Hal itu bertujuan untuk mengonfirmasi detail seperti barang-barang yang akan didonasikan, lokasi tempat donasi, metode pengiriman barang donasi, dan detail lainnya. <br>
                Selanjutnya, setelah pihak panti sosial menerima donasi, mohon untuk mengubah status penerimaan donasi melalui halaman Riwayat Donatur. Panti sosial dapat mengakses halaman Riwayat Donatur untuk kegiatan donasi tertentu dengan cara: Memilih salah satu kegiatan donasi → Scroll ke bagian bawah dan menekan tombol “Lihat Riwayat Donasi” → Tekan tombol “Konfirmasi Diterima”.
             <br>
             <br>
             <span style="font-weight: bold;">KEGIATAN RELAWAN:</span> <br>
             Apabila notifikasi berasal dari pendaftaran relawan, maka langkah selanjutnya yang pihak panti sosial harus lakukan adalah melakukan konfirmasi dengan menerima atau menolak calon relawan melalui halaman Riwayat Relawan. Panti sosial dapat mengakses halaman Riwayat Relawan untuk kegiatan relawan tertentu dengan cara:
            Memilih salah satu kegiatan relawan → Scroll ke bagian bawah dan menekan tombol “LIhat Riwayat Relawan” → Tekan tombol “Terima” apabila panti sosial menerima calon relawan atau tekan tombol “Tolak” apabila panti sosial menolak calon relawan.
            Selanjutnya, setelah panti sosial memberikan konfirmasi terhadap calon relawan, panti sosial harus menghubungi secara langsung melalui nomor WhatsApp yang telah dicantumkan. Hal ini bertujuan untuk mengonfirmasi detail seperti lokasi pelaksanaan kegiatan relawan, hal-hal yang perlu disiapkan, waktu kedatangan, dan detail lainnya.


